    public void carregarImagem(String caminhoImagem){
        
        ImageIcon icon = new ImageIcon(caminhoImagem);
        Image image = icon.getImage();
        Image newImage = image.getScaledInstance(jLabel.getHeight(), jLabel.getWidth(), java.awt.Image.SCALE_SMOOTH);
        jLabel.setIcon(new ImageIcon(newImage));
    }
